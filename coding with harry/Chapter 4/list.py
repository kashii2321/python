# a container to store a set of values of any data type

fruits = ["Apple" , "Orange" , 21 , 23.5 , False , "Mango"]
print(fruits[1])
fruits[1] = "Grapes"
print(fruits[1])

# we can make changes in lists but not strings making lists mutuable
# indexing starts from 0 

print(fruits[1:5])
# slicing is same as strings
