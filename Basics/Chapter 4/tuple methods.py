b = (1 , 2 , "Kashvi" , "Orange" , 23.21)
print(type(b))

# count function - counts the number of times that value appears in your tuple
no = b.count(2)
print(no)

# index function - it will return the index of first occurence of the value
b = (1 , 2 , "Kashvi" , "Orange" , 2 , 23.21)
y = b.index(2)
print(y)
