fruits = ["Apple" , "Orange" , 21 , 23.5 , False , "Mango"]

# append function - used for inserting at the end of the list
fruits.append("Python")
print(fruits)

# sort function - for sorting of list
l1 = [21 , 23 , 5 , 9 , 28 , 16 , 19]
l1.sort()
print(l1)

# reverse function - to reverse the list
l1.reverse()
print(l1)

# insert function - to insert in between the list
l1.insert(3 , 31)
print(l1)

# pop function - to delete any value from the list and return its value
l1.pop(3)
print(l1)

# if we write print(l1.pop(3)), its gonna print the value at 3rd index

