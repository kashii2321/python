# lists slicing is possible 
# list = [starting index : ending index] where ending index isnt included

Marks = [23, 21, 28, 16]
print(Marks[1:3])
# [:4] means [0:4]
# [2:] means [2: len(Marks)]
# slicing same as that in strings
