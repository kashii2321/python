List = [2, 1, 3]

# append function - used to add an element in the end 
List.append(4)
print(List)

# sort function - sorting the list in asc order
List.sort()
print(List)

# sort(reverse) function - to sort the list in desc order
List.sort(reverse= True)
print(List)

# list(reverse) function - reverses the list
List.reverse()
print(List)

# list.insert function - to insert element at specified index
List.insert(1,4)
print(List)

# list.remove function - to remove the first occurence of an element from the list
List = [1, 3, 5 ,1, 6, 1, 8]
List.remove(1)
print(List)

# list.pop function - to remove element from a specified index
List.pop(5)
print(List)


